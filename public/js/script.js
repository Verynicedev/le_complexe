function dynamicNav() {
    var mobileNav = document.getElementById("NavBarHamburger");
    if (mobileNav.style.display === "block") {
        mobileNav.style.display = "none";
    }
    else {
        mobileNav.style.display = "block";
    }
}
function closingNav() {
    var mobileNav = document.getElementById("NavBarHamburger");
    if (mobileNav.style.display === "block") {
        mobileNav.style.display = "none";
    }
}
